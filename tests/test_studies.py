def test_blind_analysis_prepares_derived_evidence_packets(tmp_path):
    blind = _load_blind_analysis_tool()

    run_dir = blind.prepare_blind_run(
        output_root=tmp_path,
        run_id="blind-test",
        seed=1234,
        mode="derived-evidence",
    )

    manifest = json.loads((run_dir / "manifest.json").read_text())
    template = json.loads((run_dir / "blinded_scores.template.json").read_text())
    key = json.loads((run_dir / "private/key.json").read_text())
    packet_paths = sorted((run_dir / "packets").glob("*/*.md"))
    evidence_paths = sorted((run_dir / "derived-evidence").glob("*/*.json"))

    assert manifest["mode"] == "derived-evidence"
    assert "derived-evidence mode avoids raw text review" in manifest["blindness_note"]
    assert len(packet_paths) == 27
    assert len(evidence_paths) == 27
    assert "promplet" not in (run_dir / "manifest.json").read_text().lower()
    assert "[T]" not in (run_dir / "blinded_scores.template.json").read_text()
    assert key["studies"]

    first_packet = packet_paths[0].read_text()
    first_evidence = json.loads(evidence_paths[0].read_text())
    assert "Mode: derived-evidence" in first_packet
    assert "## Derived metric summary" in first_packet
    assert "## Source material" not in first_packet
    assert "## Compiled output" not in first_packet
    assert "[C1]" not in first_packet
    assert "[C2]" not in first_packet
    assert "[T]" not in first_packet
    assert "WeaveMark" not in first_packet
    assert first_evidence["metrics"]["output_words"] > 0
    assert first_evidence["fact_candidate_count"] == len(
        first_evidence["fact_candidates"]
    )
    assert template["studies"][0]["variants"][0]["scores"]


