def test_blind_analysis_composes_hybrid_scores(tmp_path):
    blind = _load_blind_analysis_tool()

    run_dir = blind.prepare_blind_run(
        output_root=tmp_path,
        run_id="hybrid-test",
        seed=2468,
        mode="source-and-output",
    )
    template = json.loads((run_dir / "blinded_scores.template.json").read_text())
    qualitative_payload = {
        "schema_version": blind.SCHEMA_VERSION,
        "run_id": "hybrid-test",
        "mode": "source-and-output",
        "score_scale": "Absolute 1..7 per criterion; higher is better.",
        "score_source": "test-masked-review",
        "criteria": list(blind.MASKED_REVIEW_CRITERIA),
        "studies": [
            {
                "study_id": study["study_id"],
                "title": study["title"],
                "variants": [
                    {
                        "anonymous_id": variant["anonymous_id"],
                        "scores": {
                            criterion: 4 for criterion in blind.MASKED_REVIEW_CRITERIA
                        },
                        "notes": "Neutral masked-review fixture score.",
                    }
                    for variant in study["variants"]
                ],
            }
            for study in template["studies"]
        ],
    }
    qualitative_path = run_dir / "blinded_scores.qualitative.json"
    qualitative_path.write_text(json.dumps(qualitative_payload), encoding="utf-8")

    hybrid_path = blind.write_hybrid_scores(run_dir, qualitative_path)
    report_path = blind.reveal_scores(run_dir, hybrid_path)
    json_report = json.loads(report_path.with_suffix(".json").read_text())

    assert hybrid_path.name == "blinded_scores.hybrid.json"
    assert json_report["score_source"] == "hybrid-derived-metrics-and-masked-review"
    assert json_report["criterion_methods"]["Input readability"]["blindness"] == (
        "masked-source review"
    )
    assert "weak length/density proxies" in json_report["leak_risk_note"]
